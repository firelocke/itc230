                <h2>BIG Research #2: Galleries</h2>
                
                <p>Photos  can add richness to the web design of any website. Customers and casual browsers can become overwhelmed or bored with walls of text, and adding photos can help break things up, and give pages their own mood and personality. For those skimming a page, photos with captions can quickly offer context, highlight products or viewpoints, and turn casual browsing into true engagement.</p>
                
                <p>When using photos, professional quality photos are best. For business owners, people will equate the quality of the products with the quality of the photos--customers assume that if care isn’t taken with the photos, care will also be lacking in the product. Even for hobbyist websites and general web blogs, higher quality photos have come to be expected simply by so many other websites utilizing them. While using your own images is best, if this is not an option open to you and money to purchase images is tight, stock photos can always be used, and many websites like Unsplash and Pixabay offer high quality photos for free (<a href="https://fooplugins.com/4-rules-using-images-website/">Warner</a>).</p>

                <p>However, too many photos can be overwhelming, making a page feel cluttered or confusing. Photo galleries are an easy, interactive solution utilized by many websites. They clear up the clutter, making the site and it’s products easy to navigate. They also offer points of interest, and a reason for users to linger (<a href="https://www.neonrain.com/blog/why-your-website-photos-are-just-as-important-as-your-design/">Neon Rain</a>).</p>

                <p>Photo galleries can be created in as many diverse ways as there are websites. JQuery is often the most common way, as the Javascript functionality is fast, easy, and extremely versatile. There are hundreds of different options, ranging from the very simple to the extravagant, and from the expensive to the completely free (<a href="https://1stwebdesigner.com/jquery-gallery/">Team</a>). With the emergence of CSS3 technology, web designers can now also build slideshows using no Javascript at all (<a href="https://onextrapixel.com/how-to-create-an-animated-image-gallery-using-html-and-css3/">Morana</a>). The options are nearly endless.</p>             
                
